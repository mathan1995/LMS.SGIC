    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        String searchID = request.getParameter("searchText");
        try {
            List<MainClassifaction> mainClassificationList;
            MainClassifactionDataAccessObject mainClassification = new MainClassifactionDataAccessObject();
            mainClassificationList = mainClassification.searchMainClassification(searchID);
            int records = mainClassificationList.size();
            request.setAttribute("result", mainClassificationList);
            request.setAttribute("records", records);
            RequestDispatcher dispatcher = request.getRequestDispatcher("/MainClassification.jsp");
            dispatcher.forward(request, response);
        } catch (SQLException ex) {
            Logger.getLogger(SearchMainController.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SearchMainController.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

